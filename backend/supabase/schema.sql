-- Profiles table for user metadata
create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  email text,
  full_name text,
  avatar_url text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.user_profiles (
  user_id text primary key,
  email text not null default '',
  full_name text not null default '',
  phone text not null default '',
  city text not null default '',
  date_of_birth text not null default '',
  gender text not null default '',
  language text not null default 'en',
  blood_group text not null default '',
  conditions jsonb not null default '[]'::jsonb,
  medications text not null default '',
  notifications jsonb not null default '{}'::jsonb,
  google_avatar_url text not null default '',
  member_since text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.reports (
  id bigint generated by default as identity primary key,
  user_id text not null default '',
  patient_name text not null default '',
  report_date text not null default '',
  lab_name text not null default '',
  report_status text not null default '',
  biomarker_count integer not null default 0,
  finding_count integer not null default 0,
  source_path text not null default '',
  output_path text not null default '',
  stored_json_path text not null default '',
  source_hash text not null default '',
  report_json jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now()
);

create table if not exists public.report_chunks (
  id bigint generated by default as identity primary key,
  report_id bigint not null references public.reports(id) on delete cascade,
  text text not null default '',
  vector_json text not null default '[]',
  embedding_provider text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.biomarker_history (
  id bigint generated by default as identity primary key,
  user_id text not null default '',
  report_id bigint not null references public.reports(id) on delete cascade,
  biomarker_name text not null default '',
  value double precision,
  unit text not null default '',
  flag text not null default '',
  ref_range text not null default '',
  report_date text not null default '',
  lab_name text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.anomaly_findings (
  id bigint generated by default as identity primary key,
  user_id text not null default '',
  biomarker text not null default '',
  finding_type text not null default '',
  severity text not null default '',
  description text not null default '',
  data_points jsonb not null default '[]'::jsonb,
  metrics_json jsonb not null default '{}'::jsonb,
  detected_at timestamptz not null default now()
);

create table if not exists public.share_links (
  id text primary key,
  user_id text not null default '',
  report_ids jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default now(),
  expires_at timestamptz not null,
  view_count integer not null default 0
);

create table if not exists public.notification_outbox (
  id bigint generated by default as identity primary key,
  user_id text not null default '',
  report_id bigint not null default 0,
  subject text not null default '',
  body text not null default '',
  status text not null default 'queued',
  dedupe_key text not null unique,
  created_at timestamptz not null default now()
);

create table if not exists public.chat_sessions (
  session_id text primary key,
  user_id text not null default '',
  title text not null default '',
  summary text not null default '',
  summarized_message_count integer not null default 0,
  active_report_id bigint,
  active_report_at text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  message_count integer not null default 0
);

create table if not exists public.chat_messages (
  id bigint generated by default as identity primary key,
  session_id text not null references public.chat_sessions(session_id) on delete cascade,
  user_id text not null default '',
  role text not null default '',
  content text not null default '',
  created_at timestamptz not null default now()
);

create table if not exists public.memory_entries (
  id bigint generated by default as identity primary key,
  text text not null default '',
  text_key text not null default '',
  source text not null default '',
  user_id text not null default '',
  session_id text not null default '',
  importance double precision not null default 0.7,
  vector_json text not null default '[]',
  embedding_provider text not null default '',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  last_seen_at timestamptz not null default now(),
  access_count integer not null default 0
);

create index if not exists idx_reports_user on public.reports(user_id);
create index if not exists idx_report_chunks_report on public.report_chunks(report_id);
create index if not exists idx_biomarker_history_user on public.biomarker_history(user_id, biomarker_name);
create index if not exists idx_biomarker_history_report on public.biomarker_history(report_id);
create index if not exists idx_anomaly_findings_user on public.anomaly_findings(user_id);
create index if not exists idx_notification_outbox_user on public.notification_outbox(user_id);
create index if not exists idx_chat_sessions_user on public.chat_sessions(user_id);
create index if not exists idx_chat_messages_session on public.chat_messages(session_id);
create index if not exists idx_memory_entries_user on public.memory_entries(user_id);
create index if not exists idx_memory_entries_session on public.memory_entries(session_id);

