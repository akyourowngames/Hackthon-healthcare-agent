create policy if not exists "Users can read own profiles" on public.profiles
  for select using (auth.uid() = id);
create policy if not exists "Users can update own profiles" on public.profiles
  for update using (auth.uid() = id);

create policy if not exists "auth_can_read_own_reports" on public.reports
  for select to authenticated using ((auth.uid())::text = user_id);
create policy if not exists "auth_can_insert_reports" on public.reports
  for insert to authenticated with check ((auth.uid())::text = user_id);

create policy if not exists "auth_can_read_own_chunks" on public.report_chunks
  for select to authenticated
  using (report_id in (select id from public.reports where user_id = (auth.uid())::text));

create policy if not exists "auth_can_read_own_biomarkers" on public.biomarker_history
  for select to authenticated using ((auth.uid())::text = user_id);

create policy if not exists "auth_can_read_own_anomalies" on public.anomaly_findings
  for select to authenticated using ((auth.uid())::text = user_id);

create policy if not exists "auth_can_read_own_shares" on public.share_links
  for select to authenticated using ((auth.uid())::text = user_id);

create policy if not exists "auth_can_read_own_notifications" on public.notification_outbox
  for select to authenticated using ((auth.uid())::text = user_id);

create policy if not exists "Users can read own user profile" on public.user_profiles
  for select using (auth.uid()::text = user_id);
create policy if not exists "Users can insert own user profile" on public.user_profiles
  for insert with check (auth.uid()::text = user_id);
create policy if not exists "Users can update own user profile" on public.user_profiles
  for update using (auth.uid()::text = user_id);
create policy if not exists "Users can delete own user profile" on public.user_profiles
  for delete using (auth.uid()::text = user_id);

create policy if not exists "auth_can_rw_own_sessions" on public.chat_sessions
  for all to authenticated using ((auth.uid())::text = user_id);
create policy if not exists "auth_can_rw_own_messages" on public.chat_messages
  for all to authenticated using ((auth.uid())::text = user_id);
create policy if not exists "auth_can_rw_own_memory" on public.memory_entries
  for all to authenticated using ((auth.uid())::text = user_id);

create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.profiles (id, email, full_name)
  values (new.id, new.email, coalesce(new.raw_user_meta_data->>'full_name', ''));
  insert into public.user_profiles (user_id, email, full_name, google_avatar_url, member_since)
  values (
    new.id::text,
    coalesce(new.email, ''),
    coalesce(new.raw_user_meta_data->>'full_name', new.raw_user_meta_data->>'name', ''),
    coalesce(new.raw_user_meta_data->>'avatar_url', new.raw_user_meta_data->>'picture', ''),
    now()::date::text
  )
  on conflict (user_id) do nothing;
  return new;
end;
$$ language plpgsql security definer;

create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();
